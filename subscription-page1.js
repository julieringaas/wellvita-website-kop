function showPopUp() {
  var popUp = document.getElementById("pop-up");
  popUp.style.display = "block";
}

//  JavaScript to hide the pop-up

function hidePopUp() {
  var popUp = document.getElementById("pop-up");
  popUp.style.display = "none";
}

